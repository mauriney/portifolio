			<h1>Impacto</h1>
			<div class="content-pitch">
				<p>Muito trabalho e atenção foram necessários para oferecer uma solução a falta de recursos. Para unir investidores e incentivar a alocação de recursos em negócios de Impacto, foi criada a Bons Corações.</p>
				<p>A BC é um Negócio de Impacto que oferece uma plataforma virtual de crowdsourcing para união e aplicação de recursos em Ações de Impacto (“Crowdsourced”).</p>
				<p>Em outras palavras, diante do objetivo de mitigar e reverter os desafios decorrentes da desunião no Planeta, a BC fornece um ambiente para integrar redes de relações e atrair investidores para fazer acontecer: modelos de negócios rentáveis e que resolvem desafios socioambientais relevantes. Fomentando a criação, planejamento e execução de Ações de Impacto que objetivam, tijolo por tijolo, a construção do Mundo Bem Melhor.</p>
				<p>A BC ainda acredita que todo Trabalho merece recompensa, portanto, não há recompensa mais justa do que àquele objetivado no Bem e no Amor. Desafiamos uma barreira cultural por meio da proposição de um sistema de recompensa que incentiva, promove e desenvolve o Trabalho de Impacto.</p>
				<p>Daqui para frente, a BC tem o desafio de implementar essa Mudança. Mas essa grande obra não pode ser feita por poucos, precisamos juntos assumir a responsabilidade de cada um pelo seu papel e influenciar um enorme número de pessoas para a maior mudança de todos os tempos.</p>
				<p>Nós somos o legado do Mundo Bem Melhor, aqueles que escreverão com as nossas mãos a história do Amor.</p>
